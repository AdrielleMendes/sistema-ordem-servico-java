// Método para alterar uma OS existente
private void alterar_os() {
    // SQL corrigido com a ordem correta dos parâmetros
    String sql = "update tbos set tipo=?, situacao=?, equipamento=?, defeito=?, servico=?, tecnico=?, valor=? where os=?";
    
    try {
        pst = conexao.prepareStatement(sql);

        // Define o tipo baseado na seleção dos Radio Buttons
        if (jRadioButton1.isSelected()) {
            tipo = "Orçamento";
        } else {
            tipo = "Ordem de Serviço";
        }

        pst.setString(1, tipo);
        pst.setString(2, jComboBox1.getSelectedItem().toString());
        pst.setString(3, jTextField5.getText());
        pst.setString(4, jTextField6.getText());
        pst.setString(5, jTextField7.getText());
        pst.setString(6, jTextField8.getText());
        
        // Tratamento para evitar erro de valor decimal vazio no MySQL
        if (jTextField9.getText().isEmpty()) {
            pst.setString(7, "0");
        } else {
            pst.setString(7, jTextField9.getText().replace(",", "."));
        }

        // O parâmetro 8 (WHERE os=?) recebe o número da OS do jTextField1
        pst.setString(8, jTextField1.getText());

        // Validação de campos obrigatórios antes de enviar ao banco
        if (jTextField1.getText().isEmpty() || jTextField5.getText().isEmpty() || jTextField6.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha os campos obrigatórios");
        } else {
            int adicionado = pst.executeUpdate();
            if (adicionado > 0) {
                JOptionPane.showMessageDialog(null, "OS alterada com sucesso!");
                // Chama o método para limpar a tela após a edição confirmada
                limpar(); 
            }
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Erro ao alterar OS: " + e);
    }
}

