// Método para pesquisar clientes pelo nome e preencher a TABELA
private void pesquisar_cliente() {
   
    String sql = "select idcli, nomecli, fonecli from tbclientes where nomecli like ?";
    try {
        pst = conexao.prepareStatement(sql);
  
        pst.setString(1, jTextField3.getText() + "%");
        rs = pst.executeQuery();

        DefaultTableModel modelo = (DefaultTableModel) jTable3.getModel();
        modelo.setNumRows(0); 

        while (rs.next()) {
            modelo.addRow(new Object[]{
                rs.getString(1), // idcli
                rs.getString(2), // nomecli
                rs.getString(3)  // fonecli
            });
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(null, "Erro ao pesquisar: " + e);
    }
}
